const month = 3

switch (month) {
    case 1:
         console.log("january");
         break;
    case 2:
         console.log("february");
         break;
    case 3:
         console.log("march");
         break;
    case 4:
         console.log("april");
         break;
    case 5:
         console.log("may");
         break;
    case 6:
         console.log("jun");
         break;

    default:
        console.log("defult case match")
        break;
}